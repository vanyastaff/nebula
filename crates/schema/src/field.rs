//! Field definitions — consolidated enum (13 variants) + per-type structs + builders.
//!
//! Removed variants vs previous version: `Date`, `DateTime`, `Time`, `Color`, `Hidden`.
//! - Date-like fields → `StringField` with `hint: InputHint::{Date, DateTime, Time, Color}`.
//! - "Hidden" role → any field with `visible: VisibilityMode::Never`.

use nebula_validator::{Rule, ValueRule};
use serde::{Deserialize, Serialize};
use serde_json::{Number, Value};

use crate::{
    BooleanWidget, CodeWidget, FieldKey, FieldPath, InputHint, ListWidget, NumberWidget,
    ObjectWidget, RequiredMode, SecretWidget, SelectOption, SelectWidget, StringWidget,
    Transformer, VisibilityMode, mode::ExpressionMode,
};

// ── Macro ────────────────────────────────────────────────────────────────────

/// Generate a per-type field struct with shared base fields + type-specific extras.
///
/// Usage: `define_field!(TypeName { extra: Type = default, ... } expr: ExpressionMode::Xxx)`
///
/// Generated struct has all shared fields (key, label, description, placeholder, default,
/// visible, required, expression, group, rules, transformers) plus the extras. The generated
/// `impl` includes shared builders and a `key()` / `visible()` / `required()` / `expression()`
/// / `rules()` / `transformers()` / `default()` accessor set so structs can be used directly
/// before converting to `Field`.
macro_rules! define_field {
    ($name:ident { $($extra:ident: $ty:ty = $dflt:expr),* $(,)? } expr: $expr_dflt:expr) => {
        #[allow(missing_docs, reason = "type-specific fields are generated by macro")]
        #[non_exhaustive]
        #[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]
        pub struct $name {
            /// Stable field key.
            pub key: FieldKey,
            /// Human-readable label.
            #[serde(default, skip_serializing_if = "Option::is_none")]
            pub label: Option<String>,
            /// Optional help text.
            #[serde(default, skip_serializing_if = "Option::is_none")]
            pub description: Option<String>,
            /// Optional placeholder value.
            #[serde(default, skip_serializing_if = "Option::is_none")]
            pub placeholder: Option<String>,
            /// Optional default JSON value.
            #[serde(default, skip_serializing_if = "Option::is_none")]
            pub default: Option<Value>,
            /// Visibility mode.
            #[serde(default, skip_serializing_if = "VisibilityMode::is_default")]
            pub visible: VisibilityMode,
            /// Required mode.
            #[serde(default, skip_serializing_if = "RequiredMode::is_default")]
            pub required: RequiredMode,
            /// Expression mode.
            #[serde(default, skip_serializing_if = "ExpressionMode::is_default")]
            pub expression: ExpressionMode,
            /// Optional grouping name.
            #[serde(default, skip_serializing_if = "Option::is_none")]
            pub group: Option<String>,
            /// Validation rules.
            #[serde(default, skip_serializing_if = "Vec::is_empty")]
            pub rules: Vec<Rule>,
            /// Runtime transformers.
            #[serde(default, skip_serializing_if = "Vec::is_empty")]
            pub transformers: Vec<Transformer>,
            // Type-specific fields.
            $(pub $extra: $ty,)*
        }

        impl $name {
            /// Create a typed field with defaults.
            pub fn new(key: impl AsRef<str>) -> Self {
                Self {
                    key: FieldKey::new(key).expect("field key must be valid"),
                    label: None,
                    description: None,
                    placeholder: None,
                    default: None,
                    visible: VisibilityMode::default(),
                    required: RequiredMode::default(),
                    expression: $expr_dflt,
                    group: None,
                    rules: Vec::new(),
                    transformers: Vec::new(),
                    $($extra: $dflt,)*
                }
            }

            /// Borrow the field key.
            pub fn key(&self) -> &FieldKey {
                &self.key
            }

            /// Set field label.
            #[must_use]
            pub fn label(mut self, value: impl Into<String>) -> Self {
                self.label = Some(value.into());
                self
            }

            /// Set field description.
            #[must_use]
            pub fn description(mut self, value: impl Into<String>) -> Self {
                self.description = Some(value.into());
                self
            }

            /// Set field placeholder.
            #[must_use]
            pub fn placeholder(mut self, value: impl Into<String>) -> Self {
                self.placeholder = Some(value.into());
                self
            }

            /// Set default JSON value (builder).
            #[must_use]
            pub fn default(mut self, value: Value) -> Self {
                self.default = Some(value);
                self
            }

            /// Set visibility mode directly (builder).
            ///
            /// Use this instead of the removed `HiddenField`:
            /// `.visible(VisibilityMode::Never)` hides a field.
            #[must_use]
            pub fn visible(mut self, mode: VisibilityMode) -> Self {
                self.visible = mode;
                self
            }

            /// Mark field visible when predicate holds.
            #[must_use]
            pub fn visible_when(mut self, rule: Rule) -> Self {
                self.visible = VisibilityMode::When(rule);
                self
            }

            /// Mark field as always required.
            #[must_use]
            pub fn required(mut self) -> Self {
                self.required = RequiredMode::Always;
                self
            }

            /// Mark field required when predicate holds.
            #[must_use]
            pub fn required_when(mut self, rule: Rule) -> Self {
                self.required = RequiredMode::When(rule);
                self
            }

            /// Mark field visible and required when predicate holds.
            #[must_use]
            pub fn active_when(mut self, rule: Rule) -> Self {
                self.visible = VisibilityMode::When(rule.clone());
                self.required = RequiredMode::When(rule);
                self
            }

            /// Set expression mode directly.
            #[must_use]
            pub fn expression_mode(mut self, mode: ExpressionMode) -> Self {
                self.expression = mode;
                self
            }

            /// Forbid expression values on this field.
            #[must_use]
            pub fn no_expression(mut self) -> Self {
                self.expression = ExpressionMode::Forbidden;
                self
            }

            /// Set grouping name.
            #[must_use]
            pub fn group(mut self, g: impl Into<String>) -> Self {
                self.group = Some(g.into());
                self
            }

            /// Append an arbitrary rule.
            #[must_use]
            pub fn with_rule(mut self, rule: Rule) -> Self {
                self.rules.push(rule);
                self
            }

            /// Append a runtime transformer.
            #[must_use]
            pub fn with_transformer(mut self, transformer: Transformer) -> Self {
                self.transformers.push(transformer);
                self
            }

            /// Finalize this typed field as enum wrapper.
            #[must_use]
            pub fn into_field(self) -> Field {
                self.into()
            }
        }
    };
}

